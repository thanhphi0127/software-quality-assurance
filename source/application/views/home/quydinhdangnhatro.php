
<p style='color:blue;'>Cho thuê phòng trọ phải đăng ký kinh doanh và khai báo tạm trú</p>

<p>Vào trước tết 2012 tôi có xây dựng 10 căn nhà trọ cho học sinh, sinh viên và người lao động thuê theo tháng, do còn khoảng 2 tuần nữa là đến tết nên tôi chưa kịp làm giấy phép đăng ký kinh doanh( tôi đã treo biển cho thuê phòng trọ). Tôi dự định qua tết sẽ đi làm giấy phép ĐKKD, trong thời gian đó tôi có cho 1 hộ gia đình thuê nhà ở thì công an phường xuống kiểm tra lập biên bản vi phạm hành chính với những lỗi sau:
</p>
<p><b>1. Không có giấy phép ĐKKD</b></p>

<p><b>2. Không khai báo lưu trú hôm sau tôi nên công an phường thì cán bộ nói tôi vi phạm vào Điều 14 khoản 3 điểm a của Nghị định 73: hành nghề kinh doanh có đăng ký về an ninh trật tự mà không có giấy chứng nhận đủ điều kiện về an ninh trật tự, với khung phạt từ 5 đến 15 triệu đồng. Tôi xin hỏi công an phường xử lý như vậy có đúng không? Mong nhận được giải đáp.</b>
</p>

<p>Gửi bởi: nguyễn văn hùng </p>
<p>Trả lời có tính chất tham khảo</p>
<p>Trong trường hợp này, bạn có hai hành vi vi phạm hành chính, cụ thể như sau:</p>

<p><b>1. Hành vi kinh doanh mà không có Giấy chứng nhận đăng ký kinh doanh </b><br/>

Khoản 2 Điều 11 của Nghị định số 02/2008/NĐ-CP ngày 16/01/2008 của Chính phủ về xử phạt vi phạm hành chính trong lĩnh vực thương mại quy định như sau: “Phạt tiền từ 300.000 đồng đến 500.000 đồng đối với hành vi kinh doanh dưới hình thức hộ kinh doanh mà không có Giấy chứng nhận đăng ký kinh doanh theo quy định”.<br/>
Do vậy, bạn tiến hành kinh doanh mà chưa đăng ký kinh doanh thì sẽ bị phạt tiền từ 300.000 đồng đến 500.000 đồng.
</p>
<p><b>2. Hành vi không khai báo tạm trú</b><br/>
Theo quy định tại Điều 11 của Nghị định số 73/2010/NĐ-CP ngày 12/7/2010 của Chính phủ quy định xử phạt vi phạm hành chính trong lĩnh vực an ninh và trật tự, an toàn xã hội thì hành vi không thực hiện đúng quy định về đăng ký thường trú, đăng ký tạm trú sẽ bị phạt tiền từ 100.000 đồng đến 200.000 đồng.<br/>
Bạn căn cứ vào quy định nêu trên để áp dụng vào trường hợp của mình.
</p>